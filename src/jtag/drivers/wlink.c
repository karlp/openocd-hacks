unsigned char DMI_OP(
	unsigned long iIndex,
	unsigned char iAddr,
	unsigned long iData,
	unsigned char iOP,
	unsigned char *oAddr,
	unsigned long *oData,
	unsigned char *oOP)
{
	unsigned char Txbuf[9];
	unsigned char Rxbuf[9];
	unsigned long len;
	unsigned char *pData;
	unsigned char retrytime;
	Txbuf[0] = 0x81;
	Txbuf[1] = 0x08;
	Txbuf[2] = 0x06;
	Txbuf[3] = iAddr;
	Txbuf[4] = (unsigned char)(iData >> 24);
	Txbuf[5] = (unsigned char)(iData >> 16);
	Txbuf[6] = (unsigned char)(iData >> 8);
	Txbuf[7] = (unsigned char)(iData);
	Txbuf[8] = iOP;
	retrytime = 0;
/* RETRY: */
	len = 9;
	if (pWriteData(gIndex, 1, Txbuf, &len))
	{
		memset(Rxbuf, 0, sizeof(Rxbuf));
		len = 9;

		if (pReadData(gIndex, 1, Rxbuf, &len))
		{

			*oAddr = Rxbuf[3];
			if (oData)
			{
				pData = (unsigned char *)oData;
				*pData = Rxbuf[7];
				*(pData + 1) = Rxbuf[6];
				*(pData + 2) = Rxbuf[5];
				*(pData + 3) = Rxbuf[4];
			}
			*oOP = Rxbuf[8];

			retrytime++;

			if (Rxbuf[8] == 2 && Rxbuf[6] == 3)
			{
				readmcause();
			}

			return true;
		}
	}
	return false;
}

